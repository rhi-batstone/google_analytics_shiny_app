# =================================================================================================================== #
# Libraries ####
# =================================================================================================================== #
library(synthpop)
library(tidyverse)


# =================================================================================================================== #
# Read Google Analytics Data from CSV ####
# =================================================================================================================== #
location_data_raw <- read_csv("data/location-data-raw.csv")


# =================================================================================================================== #
# Synthesise Data ####
# =================================================================================================================== #
location_data_syn <- syn(
  data = location_data_raw,
  # Only synthesise number of sessions data
  visit.sequence = c("sessions")
)

# Extract synthetic data tibble
location_data_syn_tbl <- location_data_syn$syn


# =================================================================================================================== #
# Write Synthesised Data to CSV ####
# =================================================================================================================== #

write_csv(
  x = location_data_syn_tbl,
  path = "data/location-data.csv"
)
